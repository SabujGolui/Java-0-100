package Collection_Practice;

import java.util.HashSet;

public class HashSetExample3 {
	public static void main(String[] args) {
		HashSet hashSet = new HashSet();
		hashSet.add(3.14);
		hashSet.add(2.718);
		hashSet.add(1.414);
		hashSet.add(1.618);
		hashSet.add(2.236);
		System.out.println("HashSet: " + hashSet);
	}
}
