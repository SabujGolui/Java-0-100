package Collection_Practice;

import java.util.HashSet;

public class HashSetExample2 {
	public static void main(String[] args) {
		HashSet hashSet = new HashSet();
		hashSet.add(5);
		hashSet.add(2);
		hashSet.add(8);
		hashSet.add(1);
		hashSet.add(7);
		System.out.println("HashSet: " + hashSet);
	}
}
