package Collection_Practice;

import java.util.HashSet;

public class HashSetExample4 {
	public static void main(String[] args) {
		HashSet hashSet = new HashSet();
		hashSet.add('c');
		hashSet.add('a');
		hashSet.add('b');
		hashSet.add('z');
		hashSet.add('m');
		System.out.println("HashSet: " + hashSet);
	}
}
