package Collection_Practice;

import java.util.HashSet;

public class HashSetExample1 {
	public static void main(String[] args) {
		HashSet hashSet = new HashSet();
		hashSet.add("apple");
		hashSet.add("banana");
		hashSet.add("orange");
		hashSet.add("grape");
		hashSet.add("kiwi");
		System.out.println("HashSet: " + hashSet);
	}
}
