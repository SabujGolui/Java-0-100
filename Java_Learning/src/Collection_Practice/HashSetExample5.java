package Collection_Practice;

import java.util.HashSet;

public class HashSetExample5 {
	public static void main(String[] args) {
		HashSet hashSet = new HashSet();
		hashSet.add("Java");
		hashSet.add("Python");
		hashSet.add("C++");
		hashSet.add("JavaScript");
		hashSet.add("Ruby");
		System.out.println("HashSet: " + hashSet);
	}
}
